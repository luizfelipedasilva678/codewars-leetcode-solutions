/**
 * @param {number[][]} obstacleGrid
 * @return {number}
 */
function uniquePathsWithObstacles(obstacleGrid, m = 0, n = 0, memo = {}) {
  const key = `${m}-${n}`;

  if (memo[key]) return memo[key];
  if (
    m > obstacleGrid.length - 1 ||
    n > obstacleGrid[0].length - 1 ||
    obstacleGrid[m][n] === 1
  )
    return 0;
  if (m === obstacleGrid.length - 1 && n === obstacleGrid[0].length - 1)
    return 1;

  memo[key] =
    uniquePathsWithObstacles(obstacleGrid, m, n + 1, memo) +
    uniquePathsWithObstacles(obstacleGrid, m + 1, n, memo);

  return memo[key];
}

